        void Sote(long[] ar)
        {
            int i = 0;
            long[] baf = new long[4];

            while (ar[i] != -1)
            {
                if (ar[i] > ar[i + 4] && ar[i + 4] != -1)
                {
                    for (int j = 0; j < 4; j++)
                    {
                        baf[j] = ar[i + j];
                        ar[i + j] = ar[i + 4 + j];
                        ar[i + 4 + j] = baf[j];
                    }
                    if (i >= 4) i -= 4;
                }
                else
                {
                    i += 4;
                }
            }
        }

